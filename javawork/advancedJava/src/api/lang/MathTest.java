package api.lang;
//Math클래스 - 수학적인 기능을 만들어 놓은 클래스
public class MathTest {
	public static void main(String[] args) {
		System.out.println("Math.PI=>"+Math.PI);
		System.out.println("Math.abs(-1)=>"+Math.abs(-1));
		System.out.println("Math.ceil(10.4)=>"+Math.ceil(10.4));
		System.out.println("Math.round(10.5)=>"+Math.round(10.5));
		System.out.println("Math.floor(10.6)=>"+Math.floor(10.6));
		System.out.println("Math.max(100,10)=>"+Math.max(100,10));
		System.out.println("Math.min(100,10)=>"+Math.min(100,10));
		System.out.println("Math.random()=>"+Math.random());
	}
}
